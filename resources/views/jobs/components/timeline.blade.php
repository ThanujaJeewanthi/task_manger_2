function initTimeline() {
    generateDates();
    positionTasks();
    addTodayMarker();
    checkScrollNeed();
}

function generateDates() {
    const container = document.getElementById('timelineDates');
    if (!container) return;

    const startDate = new Date(document.getElementById('startDate').value);
    const endDate = new Date(document.getElementById('endDate').value);
    const today = new Date();

    container.innerHTML = '';
    let current = new Date(startDate);

    while (current <= endDate) {
        const col = document.createElement('div');
        col.className = 'date-column';

        const day = current.getDate();
        const month = current.toLocaleDateString('en', { month: 'short' });

        if (current.toDateString() === today.toDateString()) {
            col.classList.add('today');
        }
        if (current.getDay() === 0 || current.getDay() === 6) {
            col.classList.add('weekend');
        }

        col.innerHTML = `<div style="font-size: 0.7rem; color: #6c757d;">${month}</div><div>${day}</div>`;
        container.appendChild(col);

        current.setDate(current.getDate() + 1);
    }
}

function positionTasks() {
    const startDate = new Date(document.getElementById('startDate').value);
    const endDate = new Date(document.getElementById('endDate').value);
    const totalDays = Math.ceil((endDate - startDate) / (1000 * 60 * 60 * 24)) + 1;
    const dayWidth = 60;

    // Set the width of each task timeline
    document.querySelectorAll('.task-timeline').forEach(timeline => {
        timeline.style.width = (totalDays * dayWidth) + 'px';
    });

    document.querySelectorAll('.timeline-row').forEach(row => {
        const taskBar = row.querySelector('.task-bar');
        if (!taskBar) return;

        const taskStart = taskBar.dataset.start ? new Date(taskBar.dataset.start) : null;
        const taskEnd = taskBar.dataset.end ? new Date(taskBar.dataset.end) : null;

        // Hide tasks completely outside the date range
        if (!taskStart || !taskEnd || taskEnd < startDate || taskStart > endDate) {
            row.style.display = 'none';
            return;
        }

        // Show tasks that fall within or overlap the range
        row.style.display = 'flex';

        // Calculate position in pixels
        const effectiveStart = new Date(Math.max(taskStart, startDate));
        const effectiveEnd = new Date(Math.min(taskEnd, endDate));

        const startOffset = Math.ceil((effectiveStart - startDate) / (1000 * 60 * 60 * 24));
        const duration = Math.ceil((effectiveEnd - effectiveStart) / (1000 * 60 * 60 * 24)) + 1;

        const leftPx = startOffset * dayWidth;
        const widthPx = Math.max(duration * dayWidth, 60); // Minimum 60px width

        taskBar.style.left = leftPx + 'px';
        taskBar.style.width = widthPx + 'px';
        taskBar.style.position = 'absolute';
    });
}

function addTodayMarker() {
    const startDate = new Date(document.getElementById('startDate').value);
    const endDate = new Date(document.getElementById('endDate').value);
    const today = new Date();
    const dayWidth = 60;

    document.querySelectorAll('.today-marker').forEach(m => m.remove());

    if (today >= startDate && today <= endDate) {
        const dayOffset = Math.ceil((today - startDate) / (1000 * 60 * 60 * 24));
        const leftPx = dayOffset * dayWidth;

        document.querySelectorAll('.task-timeline').forEach(timeline => {
            const marker = document.createElement('div');
            marker.className = 'today-marker';
            marker.style.left = leftPx + 'px';
            timeline.appendChild(marker);
        });
    }
}

function checkScrollNeed() {
    const datesContainer = document.querySelector('.timeline-dates-container');
    const datesContent = document.querySelector('.timeline-dates');
    const instructions = document.getElementById('scrollInstructions');

    if (datesContainer && datesContent && instructions) {
        if (datesContent.scrollWidth > datesContainer.clientWidth) {
            instructions.style.display = 'block';
        } else {
            instructions.style.display = 'none';
        }
    }
}

function setupScrollSync() {
    const headerDatesContainer = document.querySelector('.timeline-dates-container');
    const taskTimelineContainers = document.querySelectorAll('.task-timeline-container');

    if (!headerDatesContainer || taskTimelineContainers.length === 0) return;

    let isScrolling = false;

    headerDatesContainer.addEventListener('scroll', function() {
        if (isScrolling) return;
        isScrolling = true;

        taskTimelineContainers.forEach(container => {
            container.scrollLeft = this.scrollLeft;
        });

        setTimeout(() => { isScrolling = false; }, 10);
    });

    taskTimelineContainers.forEach((container, index) => {
        container.addEventListener('scroll', function() {
            if (isScrolling) return;
            isScrolling = true;

            headerDatesContainer.scrollLeft = this.scrollLeft;

            taskTimelineContainers.forEach((otherContainer, otherIndex) => {
                if (otherIndex !== index) {
                    otherContainer.scrollLeft = this.scrollLeft;
                }
            });

            setTimeout(() => { isScrolling = false; }, 10);
        });
    });
}

function selectTask(taskId) {
    // Remove previous selection
    document.querySelectorAll('.timeline-row').forEach(row => row.classList.remove('selected'));

    // Add selection to current row
    const selectedRow = document.querySelector(`[data-task-id="${taskId}"]`);
    if (selectedRow) {
        selectedRow.classList.add('selected');
    }

    // Fetch and display task details
    fetch(`/jobs/${jobId}/tasks/${taskId}/details`)
        .then(response => response.json())
        .then(data => {
            displayTaskDetails(data);
        })
        .catch(error => {
            console.error('Error fetching task details:', error);
        });
}
function displayTaskDetails(data) {
    const panel = document.getElementById('taskDetails');

    // Update task info
    document.getElementById('taskName').textContent = data.task.name;
    document.getElementById('taskDescription').textContent = data.task.description;

    // Timeline info with time components
    if (data.users.length > 0) {
        const user = data.users[0];
        let timelineText = 'Not set';

        if (user.start_date && user.end_date) {
            const startDate = new Date(user.start_date).toLocaleDateString();
            const endDate = new Date(user.end_date).toLocaleDateString();

            // Add time if available
            if (user.start_time && user.end_time) {
                timelineText = `${startDate} ${user.start_time} - ${endDate} ${user.end_time}`;
            } else {
                timelineText = `${startDate} - ${endDate}`;
            }
        }

        document.getElementById('taskTimeline').textContent = timelineText;

        // Display planned duration (original duration)
        if (user.formatted_planned_duration) {
            document.getElementById('taskPlannedDuration').textContent = user.formatted_planned_duration;
        } else if (user.formatted_duration) {
            document.getElementById('taskPlannedDuration').textContent = user.formatted_duration;
        }

        // Calculate and display time remaining
        calculateAndDisplayTimeRemaining(user);
    }

    // Update progress
    const progressBar = document.getElementById('taskProgressBar');
    const progressText = document.getElementById('taskProgressText');
    if (progressBar && progressText) {
        progressBar.style.width = data.task.progress + '%';
        progressText.textContent = data.task.progress + '%';
    }

    // Update users
    const usersContainer = document.getElementById('taskUsers');
    if (usersContainer) {
        usersContainer.innerHTML = '';
        data.users.forEach(user => {
            const userDiv = document.createElement('div');
            userDiv.className = 'badge bg-secondary me-2 mb-1';
            userDiv.innerHTML = `${user.name} <small>(${user.status})</small>`;
            usersContainer.appendChild(userDiv);
        });
    }

    panel.style.display = 'block';
}


function calculateAndDisplayTimeRemaining(user) {
    const timeRemainingElement = document.getElementById('taskTimeRemaining');
    if (!timeRemainingElement) return;

    if (!user.end_date || !user.end_time) {
        timeRemainingElement.textContent = 'Not set';
        return;
    }

    // Create end datetime - use 23:59:59 if no time specified
    const endTime = user.end_time || '23:59';
    const endDateTime = new Date(`${user.end_date} ${endTime}:59`);
    const now = new Date();

    // Calculate difference in milliseconds
    const diffMs = endDateTime - now;

    if (Math.abs(diffMs) < 60000) { // Less than 1 minute
        timeRemainingElement.textContent = '0 hours remaining';
        return;
    }

    // Calculate time components
    const absDiffMs = Math.abs(diffMs);
    const totalMinutes = Math.floor(absDiffMs / (1000 * 60));
    const days = Math.floor(totalMinutes / (24 * 60));
    const hours = Math.floor((totalMinutes % (24 * 60)) / 60);
    const minutes = totalMinutes % 60;

    let timeText = '';

    // For very short durations (< 1 hour) - show only minutes
    if (totalMinutes < 60) {
        timeText = totalMinutes + ' minute' + (totalMinutes !== 1 ? 's' : '');
    }
    // For short durations (< 1 day) - show hours and minutes
    else if (days === 0) {
        if (hours > 0) {
            timeText += hours + ' hour' + (hours !== 1 ? 's' : '');
        }
        if (minutes > 0) {
            timeText += (timeText ? ' ' : '') + minutes + ' minute' + (minutes !== 1 ? 's' : '');
        }
    }
    // For longer durations - show days and hours
    else {
        if (days > 0) {
            timeText += days + ' day' + (days !== 1 ? 's' : '');
        }
        if (hours > 0) {
            timeText += (timeText ? ' ' : '') + hours + ' hour' + (hours !== 1 ? 's' : '');
        }
    }

    // Format based on whether it's overdue or remaining
    if (diffMs < 0) {
        // Overdue - show negative format
        timeRemainingElement.innerHTML = `<span class="text-danger">${timeText} overdue</span>`;
    } else {
        // Still time remaining
        timeRemainingElement.innerHTML = `<span class="text-success">${timeText} remaining</span>`;
    }
}

function formatDuration(durationInRealDays) {
    if (!durationInRealDays || durationInRealDays <= 0) {
        return '0 minutes';
    }

    const days = Math.floor(durationInRealDays);
    const hours = (durationInRealDays - days) * 24;
    const wholeHours = Math.floor(hours);
    const minutes = Math.round((hours - wholeHours) * 60);

    let formatted = '';

    // For very short durations (< 1 hour) - show only minutes
    if (durationInRealDays < (1/24)) {
        const totalMinutes = Math.round(durationInRealDays * 24 * 60);
        return totalMinutes + ' minute' + (totalMinutes !== 1 ? 's' : '');
    }

    // For short durations (< 1 day) - show hours and minutes
    if (days === 0) {
        if (wholeHours > 0) {
            formatted += wholeHours + ' hour' + (wholeHours !== 1 ? 's' : '');
        }
        if (minutes > 0) {
            formatted += (formatted ? ' ' : '') + minutes + ' minute' + (minutes !== 1 ? 's' : '');
        }
        return formatted || '0 minutes';
    }

    // For longer durations - show days, hours, minutes
    if (days > 0) {
        formatted += days + ' day' + (days !== 1 ? 's' : '');
    }
    if (wholeHours > 0) {
        formatted += (formatted ? ' ' : '') + wholeHours + ' hour' + (wholeHours !== 1 ? 's' : '');
    }
    if (minutes > 0) {
        formatted += (formatted ? ' ' : '') + minutes + ' minute' + (minutes !== 1 ? 's' : '');
    }

    return formatted || '0 minutes';
}
